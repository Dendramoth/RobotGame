package MapGridTable;

/**
 *
 * @author Dendra
 */
public class GridCellPossition {
    private int possX;
    private int possY;

    public GridCellPossition(int possX, int possY) {
        this.possX = possX;
        this.possY = possY;
    }

    public int getPossX() {
        return possX;
    }

    public int getPossY() {
        return possY;
    }
    
    
}
